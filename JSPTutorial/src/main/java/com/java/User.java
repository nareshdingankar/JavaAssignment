package com.java;

public class User {
	//student properties
	private String name;
	private String rollNo;
 
	//no-argument constructor
	public User(){
 
	}
 
	public String getName() {
		return name;
	}
 
	public void setName(String name) {
		this.name = name;
	}
 
	public String getRollNo() {
		return rollNo;
	}
 
	public void setRollNo(String rollNo) {
		this.rollNo = rollNo;
	}	
}


